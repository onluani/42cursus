#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	len;
	char	*copy;
	size_t	i;

	len = ft_strlen(str);
	copy = (char *)malloc(sizeof(char) * (len +1));
	if (!copy)
		return (NULL);
	i = 0;
	while (i < len)
	{
		copy[i] = str[i];
		i++;
	}
	copy[len] = '\0';
	return (copy);
}
